	protected function render() {
        $settings = $this->get_settings_for_display();

        $this->add_inline_editing_attributes( 'title', 'basic' );
        $this->add_render_attribute( 'title', 'class', 'section__heading--title' );


        $this->add_inline_editing_attributes( 'description', 'intermediate' );
        $this->add_render_attribute( 'description', 'class', '' );

        $title = bdevs_element_kses_basic( $settings['title' ] );

        if ( empty( $settings['slides'] ) ) {
            return;
        }
        ?>

        <?php if ( $settings['design_style'] === 'style_20' ): ?>

        <?php else: 

        if ( !empty($settings['image']['id']) ) {
            $image = wp_get_attachment_image_url( $settings['image']['id'], $settings['thumbnail_size'] );
            if ( ! $image ) {
                $image = $settings['image']['url'];
            }
        } 

        ?>
        <section class="about__area about__area--5">
            <div class="about__area--5-bg" data-background="<?php echo esc_attr($image); ?>"></div>
            <div class="container">
                <div class="row">
                    <div class="col-xl-12">
                        <div class="about__wrap about__wrap--2">
                            <div class="row">
                                <div class="col-xl-6 col-lg-6 offset-lg-5 offset-xl-5 pr-80">
                                    <div class="section__heading mb-30">
                                        <?php if ( !empty($settings['sub_title']) ) : ?>
                                        <h4 class="section__heading--title-small"><?php echo bdevs_element_kses_intermediate( $settings['sub_title'] ); ?></h4>
                                        <?php endif; ?>
                                        <?php printf( '<%1$s %2$s>%3$s</%1$s>',
                                                tag_escape( $settings['title_tag'] ),
                                                $this->get_render_attribute_string( 'title' ),
                                                $title
                                            ); ?>
                                        <?php if ( !empty($settings['description']) ) : ?>
                                            <p><?php echo bdevs_element_kses_intermediate( $settings['description'] ); ?></p>
                                            <?php endif; ?>
                                    </div>
                                    <div class="skill">
                                    <?php foreach ( $settings['slides'] as $slide ) :?>
                                        <div class="skill__box mb-30">
                                            <h4 class="title"><?php echo bdevs_element_kses_intermediate( $slide['description'] ); ?></h4>
                                            <div class="progress">
                                                <div class="progress-bar wow slideInLeft" role="progressbar"
                                                    style="width: <?php echo esc_attr( intval($slide['number']) ); ?>%; visibility: visible; animation-duration: 1s; animation-delay: 0.5s; animation-name: slideInLeft;" aria-valuemin="0" aria-valuemax="100" data-wow-duration="1s"
                                                    data-wow-delay=".5s">
                                                    <span><?php echo bdevs_element_kses_basic( $slide['title'] ); ?></span>
                                                </div>
                                            </div>
                                        </div>
                                    <?php endforeach; ?>
                                    </div>
                                </div>
                            </div>
                        </div>
                    </div>
                </div>
            </div>
        </section>

        <?php endif; ?>   
        <?php
    }
