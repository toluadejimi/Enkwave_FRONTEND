    protected function render()
    {
        $settings = $this->get_settings_for_display();


        ?>

        <?php if ($settings['design_style'] === 'style_2'):
        $this->add_render_attribute('button', 'class', 'icon_btn'); ?>

        <div class="project__slider">
            <div class="project__slider-active owl-carousel">
                <?php foreach ($settings['projects'] as $key => $slide) :
                    if (!empty($slide['image']['id'])) {
                        $image = wp_get_attachment_image_url($slide['image']['id'], $settings['thumbnail_size']);
                    }
                    $this->add_render_attribute('button', 'href', esc_url($slide['button_link']['url']), true);
                    ?>
                    <div class="project__item ">
                        <div class="casestudy__box">
                            <?php if (!empty($image)): ?>
                            <div class="casestudy__box--thumb">
                                <img src="<?php echo esc_url($slide['image']['url']); ?>" alt="Hello"/>
                                <div class="project__icon transition-3 p-absolute">
                                    <a class="popup-image" href="<?php echo esc_url($slide['image']['url']); ?>"><i
                                                class="fal fa-plus"></i></a>
                                </div>
                                <?php endif; ?>
                            </div>
                            <div class="casestudy__box--content">
                                <h5 class="sub-title"><?php echo bdevs_element_kses_basic($slide['sub_title']); ?></h5>
                                <h4 class="title"><a
                                            href="<?php echo esc_url($slide['button_link']['url']); ?>"><?php echo bdevs_element_kses_basic($slide['title']); ?></a>
                                </h4>
                            </div>
                        </div>
                    </div>
