    protected function render()
    {
        $settings = $this->get_settings_for_display();
        if (empty($settings['slides'])) {
            return;
        }
        ?>

        <?php if ($settings['design_style'] === 'style_2'): ?>
    <?php else: ?>
        <section class="project__area">
            <div class="container">
                <div class="row">
                    <div class="col-xl-12">
                        <div class="project__slider owl-carousel wow fadeInUp" data-wow-delay=".6s">
                            <?php foreach ($settings['slides'] as $slide) :
                                if (!empty($slide['image']['id'])) {
                                    $image = wp_get_attachment_image_url($slide['image']['id'], $settings['thumbnail_size']);
                                }
                                ?>
                                <div class="project__item grey-bg-19">
                                    <div class="project__content">
                                        <?php if (!empty($slide['sub_title'])) : ?>
                                            <div class="project__no">
                                                <h5><?php echo bdevs_element_kses_basic($slide['sub_title']); ?></h5>
                                            </div>
                                        <?php endif; ?>
                                        <?php if (!empty($slide['title'])) : ?>
                                            <h3>
                                                <a href="<?php echo esc_url($slide['slide_url']); ?>">
                                                    <?php echo bdevs_element_kses_basic($slide['title']); ?>
                                                </a>
                                            </h3>
                                        <?php endif; ?>
